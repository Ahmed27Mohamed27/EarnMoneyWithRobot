package com.example.robot.Model;

public class UserModel {
    String name,email,uid,password,phone,deviceID, referCode;

    String image;

    public UserModel(String name, String email, String uid, String password, String phone, String deviceID, String referCode, String image) {
        this.name = name;
        this.email = email;
        this.uid = uid;
        this.password = password;
        this.phone = phone;
        this.deviceID = deviceID;
        this.referCode = referCode;
        this.image = image;
    }

    public String getReferCode() {
        return referCode;
    }

    public void setReferCode(String referCode) {
        this.referCode = referCode;
    }

    public UserModel(){}

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getDeviceID() {
        return deviceID;
    }

    public void setDeviceID(String deviceID) {
        this.deviceID = deviceID;
    }
}
